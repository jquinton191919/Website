<h1>The Bible's Anti-Intellectual Stance</h1>
      <hr> <br />
      <font size="1">from <a href="http://www.geocities.com/paulntobin/reasonbible.html" target="_blank">The Rejection of Pascal's Wager</a></font>
      <p>
Christianity, in its essence, is fundamentally anti-reason. This anti-rationalistic aspect of Christianity is clearly seen throughout its history. Even at one 
of its earliest roots, the gospels, this aspect is already discernable. In the gospel of Luke we find the following passage:
<p>
<table class="quote">
<tr>
<td>
<b>Luke 10:21</b>
<br>
At that same hour Jesus rejoiced in the Holy Spirit and said, "I thank you, Father, Lord of heaven and earth, because you have hidden these things from the wise and the intelligent and have revealed them to infants; yes, Father, for such was your gracious will."
</td>
</tr>
</table>
<p>
In that one passage, critical reasoning, knowledge ("wise") and education ("learned") is criticized by Jesus. To the Christians it will be the ignorant, the 
unlearned and the uncritical mind that will receive God's revelation. There is another saying attributed to Jesus, this time in Matthew, that is of the same genre:
<p>
<table class="quote">
<tr>
<td>
<b>Matthew 18:3</b>
<br>
"Truly I tell you, unless you change and become like children, you will never enter the kingdom of heaven."
</td>
</tr>
</table>
<p>
The above saying had been repeated so many times in theological exhortations to piety that one wonders whether its actual implications are understood. 
To become "like little children" one must eschew critical thinking and make no use of any higher education. Little children normally have unquestioning faith; 
which is why parents normally warn them to keep away from strangers! Yet, it is this attitude that is glorified in Christianity from the earliest times until today.

The apostle Paul carried this glorification of ignorance and unreason even further. In his epistle to the Corinthians he told his them that intelligence, 
reason and education are all useless:
<p>
<table class="quote">
<tr>
<td>
<b>I Corinthians 1:18-25</b>
<br>
For the message of the cross is foolishness to those who are perishing, but to us who are saved it is the power of God. For it is written:
I will destroy the wisdom of the wise; the intelligence of the intelligent I will frustrate. [Isaiah 29:14]
Where is the wise man? Where is the scholar? Where is the philosopher of this age? Has not God made foolish the wisdom of this world? For since in the wisdom of God the world through its wisdom did not know him, God was pleased through foolishness of what was preached to save those who believe ... Greeks look for wisdom, but ... the foolishness of God is wiser than man's wisdom, and the weakness of God is stronger than man's strength.
</td>
</tr>
</table>
<p>
With such teachings it is not surprising that most Christians, laymen and theologians, would simply shrug away evidence against their faith by saying that 
the wisdom of man cannot understand God's. We can be pretty sure that the recipients of Paul's epistle above would have been pleased to read the above passage. 
For according to Paul himself, they were not particularly learned or intelligent:
<p>
<table class="quote">
<tr>
<td>
<b>I Corinthians 1:26-27</b>
<br>
Brothers, think of what you were when you were called. Not many of you were wise by human standards; not many were influential; not many of noble birth. But God chose the foolish things of the world to shame the wise.
</td>
</tr>
</table>
<p>
In the same epistle, Paul set up the first Christian defence against intellectual and scientific criticism. He argued that it is only by achieving ignorance 
and foolishness that one finally attains God's promise.
<p>
<table class="quote">
<tr>
<td>
<b>I Corinthians 3:18-20</b>
<br>
Do not deceive yourselves. If anyone of you thinks he is wise by the standards of this age, he should become a fool so that he may become wise. For the wisdom of the world is foolishness in God's sight. As it is written:
He catches the wise in their craftiness. [Job 5:13]
and again:
The Lord knows that the thought of the wise are futile. [Psalms 94:11]
</td>
</tr>
</table>
<p>
Paul's passage above shows that, ultimately, reason has no place in Christianity. As the ex-nun Karen Armstrong asks:

Are all men's thoughts - Einstein's, Pasteur's, Plato's - useless? ... [Paul's attitude] - flung Christendom into the Dark Ages, by denying human achievements of 
learning and culture ... haven't we all met Christians who use the teaching of Paul to adopt a contrived Philistinism, a denial of intellect and culture, that 
makes a great display of superiority? Then there are the fundamentalists, who refuse to look at Biblical criticism ... At the opposite pole ... Catholic teaching 
on contraception goes against all charity, all wisdom, but it is an assertion of "God's foolishness" in the face of the wisdom of the world.
<p>
Armstrong is right. Paul, more than anyone else, was responsible for pushing the western world into the dark ages; an age of intellectual oblivion that was to 
ensnare Europe for more than a thousand years. For it was Paul who started the process rolling; telling believers that it was okay if they were uneducated, okay 
if they were ignorant, okay if you don't think rationally. He, or one of his disciples, warned the Colossians against rational and reflective thinking:
<p>
<table class="quote">
<tr>
<td>
<b>Colossians 2:8</b>
<br>
Beware lest any man spoil you through philosophy...
</td>
</tr>
</table>
<p>
The stage was thus set by the writers of the New Testament to drown the light of reason.
<p>
<table class="quote">
<tr>
<td>
<b>Oswald Chambers in "My Utmost for His Highest"</b>
<p>
Simplicity is the secret to seeing things clearly. A saint does not think clearly until a long time passes, but a saint ought to see clearly without any difficulty. You cannot think through spiritual confusion to make things clear; to make things clear, you must obey. In intellectual matters you can think things out, but in spiritual matters you will only think yourself into further wandering thoughts and more confusion. If there is something in your life upon which God has put His pressure, then obey Him in that matter. Bring all your “arguments and . . . every thought into captivity to the obedience of Christ” regarding the matter, and everything will become as clear as daylight to you (2 Corinthians 10:5). Your reasoning capacity will come later, but reasoning is not how we see. We see like children, and when we try to be wise we see nothing (Matthew 11:25).
<p>
Even the very smallest thing that we allow in our lives that is not under the control of the Holy Spirit is completely sufficient to account for spiritual confusion, and spending all of our time thinking about it will still never make it clear. Spiritual confusion can only be conquered through obedience. As soon as we obey, we have discernment. This is humiliating, because when we are confused we know that the reason lies in the state of our mind. But when our natural power of sight is devoted and submitted in obedience to the Holy Spirit, it becomes the very power by which we perceive God’s will, and our entire life is kept in simplicity.
</td>
</tr>
