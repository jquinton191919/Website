<p>
This is basically the scientific method. This is what all scientist do, all day every day (besides petitioning for grants :) ). One of the most, if not the most, important part of this process is 
the "test" part. This is where the scientist attempts to falsify his or her hypothesis. If a hypothesis is not falsifiable then it's not science, and the subsequent parts of the scientific process 
are sullied and also are no longer science.</p>

<p>
Science rests on what is called the "Hypothetical-Deductive" model, which is a process of proposing hypotheses (during the numbered process above) that attempts to predict certain events/situations 
via deduction. After observing a complex series of events that is the object of study, a hypothetical explanation is proposed that is in agreement / coherent with the observations.
</p>

<p>
If the predictions predict observable instances that are contradicted by the experiment or observed facts, the prediction is falsified. The set of hypotheses from which the prediction was deduced 
is not so easiley falsified, because it is difficult to say which hypothesis to reject. The experiment tells us that the theory needs revision. In the case of a few or one hypothesis (like this guy 
is the father of the child) you can reject the hypothesis as false.
</p>

<p>
Note that rejecting an alternative hypothesis does not mean that your initial hypothesis is true.
</p>

<p><a name="falsify"></a>
Now, what exactly is falsifiability? Wikipedia.com defines falsifiability:<br>

<table class="quote"><tr><td>
Falsifiability is an important concept in the philosophy of science that amounts to the apparently paradoxical idea that a proposition or theory cannot be scientific if it does not admit 
consideration of the possibility of its being false.<p>

"Falsifiable" does not mean "false". For a proposition to be falsifiable, it must be possible in principle to make an observation that would show the proposition to be false, even if that 
observation has not been made. For example, the proposition "All crows are black" would be falsified by observing one white crow.
<p>
Any theory that is not falsifiable is said to be unscientific. Psychoanalytic theory, for example, is held up by followers of Popper as an example of an ideology rather than a science. 
A patient regarded by his psychoanalyst as "in denial" about his sexual orientation may be viewed as confirming he is homosexual simply by denying that he is; and if he has sex with women, 
he may be accused of trying to buttress his denials. In other words, there is no way the patient could convincingly demonstrate his heterosexuality to his analyst. This is an example of what 
Popper called a "closed circle". The proposition that the patient is homosexual is not falsifiable.</td></tr></table>
</p>

<p>
<u><b>The Philosophy of Science</b></u><a name="philo"></a>
<br>
<br>

Science assumes a few things:
<ol>
<li>I exist</li>
<li>My senses aren't deceiving me (the world sensed via empiricism (5 senses) is really there)</li>
<ul><li>2a. There exists a world independent of our senses</li></ul>
<li>The laws of nature are uniform throughout the Universe</li>
</ol>
<br>
<i>**if someone doesn't assume (2) or (2a), then they could be a <a href="http://en.wikipedia.org/wiki/Solipsism" target="_blank">solipsist</a>**</i>
</p>

<p>
Accepting these premises, science begins the process of Methodological Naturalism. What is Methodological Naturalism? It's basically the process of only accepting naturalistic explanations to 
scientific phenomenon.</p>
 
<p>Note that if "super"natural explanations were to have falsifiable / testable evidence for their existence, they would de-facto become naturalistic explanations. However, most supernaturalist, 
when attempting to test their hypotheses, suffer from ad hoc apologetics (more on that later). Science really doesn't reject supernaturalism a priori (ie a pre-supposition), but a posteriori 
(ie a conclusion).</p>

<p>One assumption of Naturalism is that "nature is uniform"... for example, Naturalists assume that the speed of light here on and around Earth is the same 10 billion light years away.</p>

<p>
<u><b>Induction</b></u><a name="induction"></a>
<br>
<br>
Induction is the method of taking the specific and extrapolating it to the general. For example I can observe that a number of cats I see are yellow. Utilizing induction, I might assume that "cats are yellow".

<p>
<u><b>Deduction</b></u><a name="deduction"></a>
<br>
<br>
Deduction is not exactly the opposite of Induction (going from the "general" to the "specific"). Deduction is the method of taking previous known data and arriving at a conclusion based on it. 
Usually, deduction makes all-encompasing statements... since it's difficult to actually have all knowledge about a particular premise, deduction can sometimes suffer from an assertion (ipse dixit - 
because you say so).</p>

<p>
An example of deductive reasoning via syllogism:<br>
Premise1. All ice is cold<br>
Premise2. I have ice in my hand<br>
Conclusion1. The ice in my hand is cold<br>
<br>

Deduction basically says that if the premises are true, then the conclusion is true.</p>

<p>
<u><b>Predictions</b></u><a name="predictions"></a>
<p>
A prediction in science is the concept of taking premises or facts to their logical conclusion. This is a <a href="http://en.wikipedia.org/wiki/Reductio_ad_absurdum" target="new">reductionist</a> strategy. 
For example, if I take 3 feet for every stride I walk, then taking that 
information to one of its logical conclusions would be to assume that it would take me 30 strides to walk 90 feet. A scientific example of predictions would be 
the existence of <a href="relative.php?topic=gen#black">black holes</a> following the premises of the Theory of Relativity, or the existence of 
<a href="evolution.php?query=erv#tableTop">endogenous retroviruses</a> using the premises of the Theory of Evolution. Predictions can be summed up by using the phrase "if this scientific theory
correctly models the world, then we should see 'xyz' phenomena".


<p>
<u><b>Proof vs. Evidence</b></u><a name="proof"></a>
<br>
<br>
Scientists never "prove" anything. However, following the scientific method they can arrive at theories that have evidence for them. Proof is a concept for mathematicians and logicians (and alcohol), 
since math works on the concept of tautologies and <i>theorems</i> (not theories). 2 = 2 is proof that 2 = 2... which is also a tautology. 2 + 2 = 4 is a proof. 
a<sup>2</sup> + b<sup>2</sup> = c<sup>2</sup> is a theorem. Proofs are unchanging, however "evidence" can always lead to 
different conclusions and support separate theories.</p>

<p>
A <a href="http://en.wikipedia.org/wiki/Theory#Science" target="_blank">scientific theory</a> is a <u>collection of observable facts, hypotheses and falsifiable conclusions based on those facts.</u> 
For example, it's a fact that humans and chimps have the same type and number of bones in their bodies. The theory based on this fact is <a href="evolution.php">Common Descent.</a>

<p> 
Using the lexicon of science, a theory is like language. Language is used to describe our thoughts and surroundings. If the language is inadequate at describing something, then the language must be
reworked. For example, the word "Internet" didn't exist 300 years ago. It does now. Does this mean that the English language is "wrong"? No. It just means that the English languages needs to update
itself to accomodate this new knowledge.</p>

<p>
A hypothesis in science is like a word. It either can or cannot describe what it intends to describe. A <b>Law</b> in science is like a <i>letter</i>. Laws are simple, consice mathematical statements
that succinctly describe very limited phenomena. The letter "a" describes the "ah" sound. The scientific law E = mc<sup>2</sup> represents the relationship between enery and mass, or the law F = ma describes the relationship
between force, mass, and acceleration.</p>

<p>
<u><b>Ad Hoc</b></u>
<br>
<br>

Ad hoc explanations are what move hypotheses and explanations of observed phenomenon outside the realm of falsifiability. What is ad hoc? From "The Skeptic's Dictionary":<br>

<table class="quote"><tr><td>
An ad hoc hypothesis is one created to explain away facts that seem to refute one’s theory. Ad hoc hypotheses are common in paranormal research and in the work of pseudoscientists. 
For example, ESP researchers have been known to blame the hostile thoughts of onlookers for unconsciously influencing pointer readings on sensitive instruments. <p>The hostile vibes, they say, 
made it impossible for them to duplicate a positive ESP experiment. Being able to duplicate an experiment is essential to confirming its validity. Of course, if this objection is taken seriously, 
then no experiment on ESP can ever fail. Whatever the results, one can always say they were caused by paranormal psychic forces, either the ones being tested or others not being tested.
</td></tr></table>
</p>

<p>
There are also in the field of science things called 'auxilliary hypotheses'. The difference is that these auxilliary hypotheses bring an increased empirical content, which is testable 
(like instrument error). Ad hoc's are not testable / falsifiable.
</p>

<p>
<u><b>Peer Review / Publishing</b></u><a name="peer"></a>
<br>
<br>
Another part of the scientific process is publishing the findings of scientific inquiry. This is known as as the peer-review process, where a scientists submits their findings to a scientific 
journal. Some journals aks you to suggests some reviewers, some don't.</p>

<p>
Either way, the editor sends your work to one to four reviewers (the number depends on the journal) - which can include the ones you suggested but doesn't have to - who are also scientists 
with working experience on what your work is about, or at least about similar topics.</p>

<p>
They then look at your work to determine if the results look reasonable, if the conclusions drawn from the result are warranted, if the quality of the work is high enough, if some references or 
data points are missing, if the work is suited to the journal or should be sent elsewhere, etc. They can then recommend publication with minor or major changes, or reject your work entirely.</p>

<p>
The editor then sends the reviews back to you, without indicating who wrote them. You have to make the corrections which the reviewers suggested or at least have to justify why they are not 
necessary. If it wasn't rejected outright, you can then submit your work again.</p>

<p>Then the editor decides based on the reviews and your answer what to do. If he think you have not answered the objections sufficiently (after all, all editors are themselves working scientists 
and are able to judge the arguments at least in part), he can reject it entirely. You can then give up or submit it to another journal. If he's unsure, he can again send it to reviewers.</p>

<p>If you're lucky, he thinks you've met the objections and will allow the publication.</p>

<p>This entire process - from submitting to publication - usually takes 6-9 months.</p>
<p>
<u><b>Ph.D.'s</b></u><a name="phd"></a>
<br>
<br>
PhD stands for "Philosophy Doctor". Before the late 19th and 20th century, most people that we would call "scientists" right now were called "Natural Philosophers".
The word "science" simply meant "knowledge". 
During this time period, other "professions" earned the title lawyer or medical doctor, and the line separating Theology and Philosophy didn't exist or was very hard to find.
Natural Philosophers studied nature to find out how God designed the world around them and discovered "laws" of nature, usually described in mathematical terms. 
<p>
Over time, the term "natural philosopher" was replaced by "scientist" in the public eye, but in universities the tradition of scientists being natural philosophers - or
philosophers of nature - stayed; with every new scientist earning the title "Doctor of Philosophy": Ph.D. The line demarcating "philosophy" and "theology", at least
in the context of universities, is drawn by distinguishing "Th.D.s" (Theology Doctor) from Ph.Ds.
<p>
Graduate students and PhD candidates are prepared for the <b>Peer Review</b> process by having to propose and submit a thesis, which they have to defend in front of a board (not necessarily
of peers!). Upon successful defense of their thesis (the equivalent of a publishing in a peer-reviewed journal), they can earn the title "Master" or "Philosophy Doctor (PhD)" in their 
designated field. Sometimes the thesis can be defended so well that <i>does</i> appear in a respectable peer-reviewed journal.
<hr>



<p>
Finally, science says nothing about "Truth" (with a capital 'T'), science just speaks about what we observe, where the evidence leads us, and what we can predict / deduce utilizing rational 
conclusions from those observations. If a scientist's prediction / hypothesis / deduction comes to fruition, this still does not encompass "Truth"; it's just a predictive model of what's going on.
</p>

